template<size_t BUFFER_SIZE = 4096>
class LineReader {
public:
LineReader(){buffer_.resize(BUFFER_SIZE);eof_=!fill_buffer();}

bool get_line(std::string& s) {
if(eof_){return false;}
size_t pos;
while ( (pos=buffer_.find('\n',used_)) == std::string::npos) {
if (eof_) {
pos = buffer_.size();
if (used_ < pos) {
break;
}
return false;
}
if (!fill_buffer()) {
eof_ = true;
}
}
s.assign(buffer_,used_,pos-used_);
used_ = pos+1;
return true;
}
private:
bool fill_buffer() {
if (lastRead_) return false;
size_t len = buffer_.size();
size_t fit,remain;
if (used_>0) {
remain = len - used_;
if (used_ < (len>>1)) {
memcpy(&buffer_[0],&buffer_[used_],remain);
}
else {
memmove(&buffer_[0],&buffer_[used_],remain);
}
fit = used_;
used_ = 0;
}
else {
fit = len;
remain = 0;
}
auto ret = ::read(0,&buffer_[remain],fit);
//std::cerr << "Reading " << fit << "/" << len << " : " << ret << "\n";
if (ret == fit) {
return true;
}
lastRead_ = true;
if (ret>0) {
buffer_.resize(remain + ret);
return true;
}
buffer_.resize(remain);
return false;
}
bool   eof_,lastRead_=false;
size_t used_ = 0;
std::string buffer_;
};
